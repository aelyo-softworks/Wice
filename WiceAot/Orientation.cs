namespace Wice;

/// <summary>
/// Specifies the layout direction for arranging UI elements or content.
/// </summary>
public enum Orientation
{
    /// <summary>
    /// Content flows along the horizontal (X) axis.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Content flows along the vertical (Y) axis, top-to-bottom.
    /// </summary>
    Vertical,
}
